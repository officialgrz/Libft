#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*src;

	src = (unsigned char *)s;
	while (n--)
	{
		if (*src != (unsigned char)c)
			src++;
		else
			return (src);
	}
	return (NULL);
}
